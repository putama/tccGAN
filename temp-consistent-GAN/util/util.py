def mkdir(path):
    if not os.path.exists(path):
        os.makedirs(path)

def mkdirreplace(path):
    if os.path.exists(path):
        shutil.rmtree(path)
    os.makedirs(path)

def load_flo(path):
    with open(path, 'rb') as f:
        magic = np.fromfile(f, np.float32, count=1)
        assert(202021.25 == magic),'Magic number incorrect. Invalid .flo file'
        h = np.fromfile(f, np.int32, count=1)[0]
        w = np.fromfile(f, np.int32, count=1)[0]
        data = np.fromfile(f, np.float32, count=2*w*h)
    # Reshape data into 3D array (columns, rows, bands)
    data2D = np.resize(data, (w, h, 2))
    return data2D

def create_log_path(opt):
    import socket
    from datetime import datetime
    info = datetime.now().strftime('%b%d_%H-%M-%S')+'_'+socket.gethostname()
    info = info + "-tempo_" + str(opt.tempo) + "-idt_" + str(opt.identity)
    
    log_dir = os.path.join('runs', info)
    return log_dir

def video_writer(direction, epoch, log_dir, filename = "video"):
    log_dir = os.path.join(log_dir, direction, str(epoch))
    mkdir(log_dir)
    frame_width = 256
    frame_height = 256
    filepath = os.path.join(log_dir, str(filename)+".avi")
    out = cv2.VideoWriter(filepath, cv2.VideoWriter_fourcc('M','J','P','G'), 20, (frame_width,frame_height))

    return out
